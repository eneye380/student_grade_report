     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $last_id = DB::table('students')->max('stud_id');
        if($last_id){
            $last_id += 1;
        }else{
            $last_id = 1000000;
        }
        
        return view('dashboard.student', compact('last_id'));
    }

