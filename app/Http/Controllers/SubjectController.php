    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $last_id = DB::table('subjects')->max('sub_id');
        if($last_id){
            $last_id += 1;
        }else{
            $last_id = 100;
        }
        $subjects = Subject::all();
        return view('dashboard.subject')->with(compact('last_id'))->with(compact('subjects'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $subject = Subject::where('name', $request->name)->first();
        if($subject){
            return redirect()->back()->with(['error'=>"$request->name already exist"]);
        }
        Subject::create($request->all());
        return redirect()->back()->with(['success'=>"$request->name added successfully"]);
    }

